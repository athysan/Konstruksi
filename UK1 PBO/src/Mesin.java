public class Mesin {
    // Encapsulation
    private String jenisMesin;

    public Mesin(String jenisMesin) {
        this.jenisMesin = jenisMesin;
    }

    // Getter dan Setter
    public String getJenisMesin() {
        return jenisMesin;
    }

    public void setJenisMesin(String jenisMesin) {
        this.jenisMesin = jenisMesin;
    }
}
